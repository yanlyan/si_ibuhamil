    <h3>Strategi, Kebijakan dan Motto Puskesmas Gorang Gareng Taji</h3>
    <p>Sumber daya manusia merupakan faktor utama dalam pembangunan nasional, dimana derajad kesehatan sangat menentukan sekali dalam pembinaan sumber daya manusia sebagai modal dasar pembangunan. Kesehatan adalah keadaan sejahtera badan, jiwa dan sosial yang memungkinkan setiap orang mampu hidup produktif secara sosial dan ekonomis (UU RI No.23 Tahun 1992), Untuk mewujudkannya diperlukan perencanaan yang strategis, mantap, terpadu dan berkesinambungan.</p>

    <p>Strategi Puskesmas Gorang Gareng Taji :</p>
    <ol>
        <li>Mengembangkan dan memanfaatkan potensi sumber daya manusia (SDM), dana, sarana, dan prasarana yang ada di Puskesmas secara optimal.</li>
        <li>Meningkatkan wawasan dan kompetensi petugas dengan mengikuti seminar dan pelatihan bagi petugas yang berhubungan dengan profesi dan tugas-tugasnya.</li>
        <li>Menggerakkan dan meningkatkan persn serta aktif masyarakat dan lintas sektor dalam memecahkan masalah-masalah kesehatan.</li>
        <li>Semua petugas selainmenerapkan ilmu yang dimiliki juga harus menerapkan etika dan kepribadian yaitu harus senyum, sapa, dan ramah pada setiap pengunjung.</li>
        <li>Meningkatkan kebersamaan dan kesejahteraan karyawan dalam menunjang kualitas pelayanan .</li>
    </ol>
    <br />
    <p>Kebijakan Puskesmas Gorang Gareng Taji :</p>
    <ul>
        <li>Menggerakkan segenap potensi yang ada di UPTD Puskesmas Gorang Gareng Taji untuk meningkatkan derajat kesehatan masyarakat dan memberikan pelayanan prima yang berorientasi pada kepuasan pengunjung.</li>
    </ul>
</div>