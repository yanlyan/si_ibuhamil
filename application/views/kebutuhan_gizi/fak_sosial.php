    <h4>- Faktor Sosial Ekonomi</h4>
    <p>
        Kondisi sosial ekonomi dalam masyarakat merupakan indikator dalam penilaian apakah dalam satu keluarga itu mampu atau tidak dalam memenuhi kebutuhan. Tingkat sosial ekonomi rendah diidentikkan dengan kemiskinan. Ibu hamil dengan tingkat sosial ekonomi rendah cenderung mengabaikan pemenuhan asupan gizi seimbang dalam kehamilan, sehingga seringkali mengalami masalah gizi dalam kehamilan, misalnya anemia dan KEK.
