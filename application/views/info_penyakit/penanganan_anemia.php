    <p class="readable-text text-home">1. Anemia Ringan</p>
    <p class="readable-text text-home ttt">
        Dengan kadar Hemoglobin 9-10 gr% masih dianggap ringan sehingga hanya perlu diberikan kombinasi 60 mg/ hari besi dan 400 mg asam folat peroral sekali sehari. (Arisman, 2004, hal.150-151).
    </p>

    <p class="readable-text text-home">2. Anemia Sedang</p>
    <p class="readable-text text-home ttt">
        Pengobatannya dengan kombinasi 120 mg zat besi dan 500 mg asam folat peroral sekali sehari. (Arisman, 2004, 150).
    </p>

    <p class="readable-text text-home">3. Anemia Berat</p>
    <p class="readable-text text-home ttt">
        Dengan kadar Hemoglobin 9-10 gr% masih dianggap ringan sehingga hanya perlu diberikan kombinasi 60 mg/ hari besi dan 400 mg asam folat peroral sekali sehari. (Arisman, 2004, hal.150-151).
    </p>
    <ul>
        <li class="text-home">Parenteral yaitu dengan fero dextrin sebanyak 1000 mg  (20 ml) intravena atau 2x10 ml intramuskuler</li>
        <li class="text-home">Transfusi darah kehamilan lanjut dapat diberikan walaupun sangat jarang diberikan mengingat resiko transfusi bagi ibu dan janin. (Sarwono Prawirohardjo, Ilmu Kebidanan hal 125).</li>
        <li class="text-home">Pemeriksaan Kadar Hb minimal Pada Tribulan I dan III</li>
        <li class="text-home">Rujuk ke Rumah Sakit</li>
    </ul>
